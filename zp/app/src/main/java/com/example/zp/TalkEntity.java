package com.example.zp;

public class TalkEntity {

    private String myTalk;
    private String id;
    private String ltdname;
    private String qyoruser;
    private String tdid;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLtdname() {
        return ltdname;
    }

    public void setLtdname(String ltdname) {
        this.ltdname = ltdname;
    }

    public String getQyoruser() {
        return qyoruser;
    }

    public void setQyoruser(String qyoruser) {
        this.qyoruser = qyoruser;
    }

    public String getTdid() {
        return tdid;
    }

    public void setTdid(String tdid) {
        this.tdid = tdid;
    }

    public String getMyTalk() {
        return myTalk;
    }

    public void setMyTalk(String myTalk) {
        this.myTalk = myTalk;
    }
}
