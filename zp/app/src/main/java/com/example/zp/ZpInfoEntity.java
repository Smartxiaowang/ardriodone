package com.example.zp;

public class ZpInfoEntity {
    private String id;
    private String salary;
    private String place;
    private String ltdid;
    private String ltdname;
    private String job;
    private String onespeak;

    public String getId(String id) {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSalary() {
        return salary;
    }

    public void setSalary(String salary) {
        this.salary = salary;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getLtdid() {
        return ltdid;
    }

    public void setLtdid(String ltdid) {
        this.ltdid = ltdid;
    }

    public String getLtdname() {
        return ltdname;
    }

    public void setLtdname(String ltdname) {
        this.ltdname = ltdname;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public String getOnespeak() {
        return onespeak;
    }

    public void setOnespeak(String onespeak) {
        this.onespeak = onespeak;
    }
}
